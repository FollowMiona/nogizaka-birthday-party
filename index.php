<script>
	jQuery(document).ready(function($){

	//	bdayObject = [];

		$("#generate").on("click", function(){

			bdayObject = {};

			$('input[type=checkbox]').each(function () {
	           if (this.checked) {
	               bdayObject[$(this).data("name")] = $(this).data("date");
	           }
			});

			if (Object.keys(bdayObject).length === 0) {
				$(".downloadlink").html('<span class="uhoh">Please select at least one member.</span>');
				return false;
			}

			$.ajax('download.php', {
			    type: 'POST',
			    dataTyp: 'json',
			    data: {
			    	bday: bdayObject
			    },
			    success: function (data, status, xhr) {
			       $(".downloadlink").html('<a href="'+data+'">Calendar generated, click here to download</a>');
			    },
			    error: function (jqXhr, textStatus, errorMessage) {
		           console.log(errorMessage);
			    }
			});
		});

		$(".checkall").on("click", function(){
			gen = $(this).data("gen");

			$("#"+gen).find("input").prop("checked", true);
		});

	});
</script>
